                <h2 style="color: #ce1a1b;">About Delhi Metro</h2>
                <p class="lh-base">
                <p>The Delhi Metro has been instrumental in ushering in a new era in the sphere of mass urban
                    transportation in India. The swanky and modern Metro system introduced comfortable, air conditioned
                    and eco-friendly services for the first time in India and completely revolutionized the mass
                    transportation scenario not only in the National Capital Region but the entire country.</p>
                <p class="lh-base">Having constructed a massive network of about 390.14 Km with 286 stations (including
                    NOIDA-Greater NOIDA Corridor and Rapid Metro, Gurugram) in record time in Delhi, NCR, the DMRC today
                    stands out as a shining example of how a mammoth technically complex infrastructure project can be
                    completed before time and within budgeted cost by a Government agency.</p>
                <p class="lh-base">The Delhi Metro Rail Corporation Limited (DMRC) was registered on 3rd May 1995 under
                    the Companies Act, 1956 with equal equity participation of the Government of the National Capital
                    Territory of Delhi (GNCTD) and the Central Government to implement the dream of construction and
                    operation of a world- class Mass Rapid Transport System (MRTS). <img src="./image/Metro-delhi.webp"
                        style="width: 470px;float: right;margin: 20px 0 20px 20px;"></p>
                <p class="lh-base">The DMRC opened its first corridor between Shahdara and Tis Hazari on 25th December,
                    2002. Subsequently, the first phase of construction worth 65 kilometres of Metro lines was finished
                    two years and nine months ahead of schedule in 2005. Since then the DMRC has also completed the
                    construction of another 125 kilometres of Metro corridors under the second phase in only four and a
                    half years.</p>
                <p class="lh-base">Presently, the Delhi Metro network consists of about 390.14 Km with 286 stations. The
                    network has now crossed the boundaries of Delhi to reach NOIDA and Ghaziabad in Uttar Pradesh,
                    Gurgaon, Faridabad, Bahadurgarh and Ballabhgarh in Haryana. With the opening of the Majlis Park to
                    Shiv Vihar and Janakpuri West - Botanical Garden Sections, new age trains equipped with the
                    Unattended Train Operation (UTO) technology have been introduced. These trains operate with the
                    Communication Based Train Control(CBTC) signaling technology which facilitate movement of trains in
                    very short frequencies. This network also includes the NOIDA - Greater NOIDA Aqua Line. The Aqua
                    Line has been constructed by DMRC on behalf of the NOIDA Metro Rail Corporation and is also being
                    operated by DMRC currently. In addition, the 11.6 kilometre long Rapid Metro also connects with the
                    Delhi Metro network at Sikanderpur station of Yellow Line. The Rapid Metro provides connectivity
                    within the satellite city of Gurugram.</p>
                <p class="lh-base">The Airport Express link between the Indira Gandhi International Airport and New
                    Delhi has now propelled Delhi to the league of global cities which have high speed rail connectivity
                    between the city and the airport. The DMRC today has over 300 train sets of four, six and eight
                    coaches.</p>
                <p class="lh-base">The Delhi Metro has also contributed tremendously on the environment front by
                    becoming the first ever railway project in the world to claim carbon credits for regenerative
                    braking. DMRC has also been certified by the United Nations (UN) as the first Metro Rail and Rail
                    based system in the world to get carbon Credits for reducing Green House gas emissions as it has
                    helped to reduce pollution levels in the city by 6.3 lakh tons every year thus helping in reducing
                    global warming.</p>
                <p class="lh-base">It has also set up roof top solar power plants at many of its stations. All stations
                    of the presently under construction corridors are being constructed as green buildings. In the
                    present phase of Delhi Metro’s construction, the DMRC has completed 160 kilometres of Metro lines
                    which has woven a web of Metro corridors along the city’s Ring Road besides connecting with many
                    other localities in NOIDA, Ghaziabad, Bahadurgarh and Ballabhgarh.</p>
                <p class="lh-base">Apart from providing Delhites with a comfortable public transport option, the Delhi
                    Metro is also contributing significantly towards controlling pollution as well as reducing vehicular
                    congestion on the roads.</p>
